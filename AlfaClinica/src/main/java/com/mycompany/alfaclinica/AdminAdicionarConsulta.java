    private void BotaoSubmeterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoSubmeterActionPerformed
        // TODO add your handling code here:
        String diaSemana = DiaSemana.getText();
        String dia = Dia.getText();
        String mes = Mes.getText();
        String ano = Ano.getText();
        String data = dia + "/" + mes + "/" + ano;
        String hora = Hora.getText();
        String medico = Medico.getText();
        String especialidade = Especialidade.getText();

        try{
            InformacoesConsulta informacoesConsulta = new InformacoesConsulta(diaSemana, data, hora, medico, especialidade);
            DAO dao = new DAO();
            dao.inserirConsulta(informacoesConsulta);
            JOptionPane.showMessageDialog(null,"Consulta inserida");
            TelaInicialAdmin telaAdmin = new TelaInicialAdmin();
            this.dispose();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "NÃ£o foi possivel inserir consulta");
            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_BotaoSubmeterActionPerformed

