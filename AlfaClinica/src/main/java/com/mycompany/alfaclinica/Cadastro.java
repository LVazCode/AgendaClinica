    private void BotaoSubmeterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoSubmeterActionPerformed
        // TODO add your handling code here:
        String nome = NomeCompleto.getText();
        String diaNascimento = DiaNascimento.getText();
        String mesNascimento = MesNascimento.getText();
        String anoNascimento = AnoNascimento.getText();
        String nascimento = anoNascimento + "-" + mesNascimento + "-" + diaNascimento;
        String email = Email.getText();
        String telefone = Telefone.getText();
        String usuario = CrieUsuario.getText();
        String senha = new String (CrieSenha.getPassword());
        
        try{
            InformacoesCadastro informacoesCadastro = new InformacoesCadastro(0, usuario, senha, nome, nascimento, email, telefone);
            DAO dao = new DAO();
            dao.inserir(informacoesCadastro);
            JOptionPane.showMessageDialog(null,"Usuario inserido!!");
            TelaLogin telaLogin = new TelaLogin();
        this.dispose();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "NÃ£o foi possivel inserir usuario");
            JOptionPane.showMessageDialog(null, e);
        }
        
        
    }//GEN-LAST:event_BotaoSubmeterActionPerformed

