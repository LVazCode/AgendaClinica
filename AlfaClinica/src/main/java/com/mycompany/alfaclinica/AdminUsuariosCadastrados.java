    public void consultar()throws Exception {
        DefaultTableModel model = (DefaultTableModel)tableConsultas.getModel();
        model.setRowCount(0);
        
        String sql = "SELECT * FROM tb_usuario";
        try(Connection conn= ConexaoBD.obtemConexao();
           PreparedStatement ps = conn.prepareStatement(sql)){
            ResultSet rs = ps.executeQuery();
            
            while(rs.next()){
                model.addRow(new String[]{rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5), rs.getString(6)});
            }
        }
    }
    
    public void usuarios()throws Exception {
        DefaultTableModel model = (DefaultTableModel)tableUsuarios.getModel();
        model.setRowCount(0);
        
        String sql = "SELECT COUNT(*) AS quantidade FROM tb_usuario;";
        try(Connection conn= ConexaoBD.obtemConexao();
           PreparedStatement ps = conn.prepareStatement(sql)){
            ResultSet rs = ps.executeQuery();
            
            while(rs.next()){
                model.addRow(new String[]{rs.getString(1)});
            }
        }
    }

